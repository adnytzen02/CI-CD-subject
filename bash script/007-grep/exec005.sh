#!/bin/bash

# [[:lower:]] –小寫字母
grep '[[:lower:]]' txt002.txt

# [[:upper:]] –大寫字母
grep '[[:upper:]]' txt002.txt

# [[:digit:]] –數字
grep '[[:digit:]]' txt002.txt

# [[:alnum:]] - 字母數字字符。
grep '[[:alnum:]]'  txt002.txt

# [[:alpha:]] –字母字符
grep '[[:alpha:]]' txt002.txt

# [[:blank:]] –空白字符
grep '[[:blank:]]' txt002.txt

# [[:space:]] –空格字符
grep '[[:space:]]' txt002.txt



